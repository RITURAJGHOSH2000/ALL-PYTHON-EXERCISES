m = input('Enter the month: ')
d = int(input('Enter the day: '))
if m == 'March' and d == 20:
    print('It\'s spring')
elif m == 'June' and d == 21:
    print('It\'s summer')
elif m == 'September' and d == 22:
    print('It\'s fall')
elif m == 'December' and d == 21:
    print('It\'s winter')
